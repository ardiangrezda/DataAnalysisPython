x5 = 'Python is a useful programming language' 
print ("x5 = ", x5)
print ("x5[10] = ", x5[10])
print ("x5[15] = ", x5[15])

print ("x5[-10] = ", x5[-10])
print ("x5[-15] = ", x5[-15])

print ("x5[10:] = ", x5[10:])
print ("x5[-10:] = ", x5[-10:])

print ("x5[:10] = ", x5[:10])
print ("x5[:-10] = ", x5[:-10])

print ("x5[10:15] = ", x5[10:15])
print ("x5[-15:-10] = ", x5[-15:-10])