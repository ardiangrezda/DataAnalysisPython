from numpy import array
x22 = array([1, 9, 7, 10, 2])

x23 = x22[:]

print ("x22 = ", x22)
print ("-----------------------------")

print ("x23 = ", x23)
print ("-----------------------------")

x24 = x22[:2]
print ("x24 = ", x24)
print ("-----------------------------")

x25 = x22[1::2]
print ("x25 = ", x25)
print ("-----------------------------")

x26 = array([
        [4, 2, 3, 1, 9],
        [15, 16, 12, 18, 11]
    ])
print ("x26 = \n", x26)
print ("-----------------------------")

print ("x26[:1, :] = ", x26[:1, :])
print ("-----------------------------")

print ("x26[:1] = ", x26[:1])
print ("-----------------------------")

print ("x26[:, :1] = \n", x26[:, :1])
print ("-----------------------------")

print ("x26[:1, 0:2] = ", x26[:1, 0:2])
print ("-----------------------------")

print ("x26[:, 2:] = \n", x26[:, 2:])
print ("-----------------------------")



